            <h4><strong>Peningkatan Mutu dan Keselamatan Pasien</strong></h4>
                <br/>
                <p class="text_justify">
                <p><strong>Pengertian</strong><br>
                    <br>
                    Dalam rangka meningkatkan mutu pelayanan pasien dan menjamin keselamatan pasien maka rumah sakit perlu mempunyai program peningkatan mutu dan keselamatan pasien (PMKP) yang menjangkau ke seluruh unit kerja di rumah sakit. Untuk melaksanakan program tersebut tidaklah mudah karena memerlukan koordinasi dan komunikasi yang baik anyata kepala bidang/divisi medis, keperawatan, penunjang medis, administrasi, dan lainnya termasuk kepala unit/departemen/instalasi pelayanan. Untuk itu, rumah sakit perlu menetapkan komite/tim atau bentuk organisasi lainnya untuk mengelola program peningkatan mutu dan keselamatan pasien agar mekanisme koordinasi pelaksanaan program peningkatan mutu dan keselamatan pasien dapat berjalan lebih baik.</p>

                    <p>RS AN-NISA sebagai Rumah Sakit yang berkomitmen dalam upaya peningkatan mutu dan keselamatan pasien membentuk sebuah unit khusus Manajemen Risiko yang terdiri dari 4 bidang, yaitu Peningkatan Mutu dan Keselamatan Pasien (PMKP), Keselamatan Pasien Rumah Sakit (KPRS/Patient Safety), Pencegahan dan Pengendalian Infeksi (PPI), serta Keselamatan dan Kesehatan Kerja Rumah Sakit (K3RS) sebagai motor penggerak pelaksanaan budaya mutu dan keselamatan.</p>

                    <p>Mutu dan keselamatan sejatinya berakar dari pekerjaan sehari-hari dari seluruh staf di unit pelayanan seperti staf klinis melakukan asesmen kebutuhan pasien dan memberikan pelayanan. Standar PMKP membantu staf klinis untuk memahami bagaimana melakukan peningkatan nyata dalam memberikan asuhan pasien dan menurunkan risiko. Demikian pula staf nonklinis dapat memasukkan standar dalam pekerjaan sehari-hari mereka untuk memahami bagaimana suatu proses dapat lebih efisien, sumber daya dapat digunakan dengan lebih bijaksana, dan risiko fisik dapat dikurangi.</p>

                    <p>Fokus area standar peningkatan mutu dan keselamatan pasien adalah:</p>

                    <ol>
                    <li>Pengelolaan kegiatan peningkatan mutu dan keselamatan pasien</li>
                    <li>Pemilihan, pengumpulan, analisis dan validasi data indikator mutu</li>
                    <li>Pelaporan dan analisis insiden keselamatan pasien</li>
                    <li>Pencapaian dan mempertahankan perbaikan</li>
                    <li>Manajemen risiko</li>
                    </ol>

                    <p><strong>Tujuan</strong><br>
                    Meningkatkan pelayanan kesehatan melalui upaya peningkatan mutu pelayanan dan keselamatan pasien di Rumah Sakit AN-NISA secara efektif dan efisien agar tercapai derajat kesehatan yang optimal dan meningkatkan kepuasan pelanggan.<br>
                    <br>
                    <strong>Tujuan Khusus</strong></p>

                    <ul>
                    <li>Mengurangi risiko insiden keselamatan pasien</li>
                    <li>Terciptanya budaya keselamatan pasien di rumah sakit</li>
                    <li>Terciptanya lingkungan kerja yang aman dan nyaman</li>
                    <li>Menurunkan angka kejadian tidak diharapkan (KTD) di rumah sakit</li>
                    <li>Terlaksananya kegiatan pencegahan sehingga tidak terjadi pengulangan kejadian yang tidak diharapkan</li>
                    <li>Meningkatkan kepuasan pasien terhadap pelayanan rumah sakit</li>
                    <li>Meningkatkan kepercayaan masyarakat terhadap pelayanan rumah sakit</li>
                    <li>Terlaksananya kegiatan peningkatan mutu dan keselamatan pasien yang berkelanjutan</li>
                    </ul>
                </p>
